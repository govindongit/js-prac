// A Set in JavaScript is used to store a unique collection of items, meaning no duplicates are allowed.

// using Array
let s1 = new Set([10, 20, 30, 30, 40]);
console.log(s1);

let s2 = new Set(["Govind", "Govind", "Nagvanshi"]);
console.log(s2);


// Using String
let s3 = new Set("Gooooovind");
console.log(s3);

let s4 = new Set();
console.log(s4);